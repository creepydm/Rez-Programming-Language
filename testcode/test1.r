
FUNC add
SUM A,B,Z,ADD
OUTVAR Z
ENDFUNC

FUNC sub
SUM A,B,Z,SUB
OUTVAR Z
ENDFUNC

FUNC mul
SUM A,B,Z,MUL
OUTVAR Z
ENDFUNC

FUNC div
SUM A,B,Z,DIV
OUTVAR Z
ENDFUNC


VAR STR 1,ADD
VAR STR 2,SUB
VAR STR 3,MUL
VAR STR 4,DIV

OUTPUT ENTER FIRST NUMBER
IN INT,A
OUTPUT ENTER SECOND NUMBER
IN INT,B
OUTPUT ENTER OPERATOR
IN STR,D

IF D,1,== CALL add
IF D,2,== CALL sub
IF D,3,== CALL mul
IF D,4,== CALL div

END
